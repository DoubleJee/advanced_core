package algorithms.sorting.comparison;

public class ComparisonCore {
    /**
     * 比较排序
     */

    /**
     * 冒泡排序：
     * 1. 从头开始比较每一对相邻元素，如果第1个比第2个大，就交换它们的位置（执行完一轮后，最末尾的元素就是最大的元素）
     * 2. 忽略步骤1中曾经找到的最大元素，重复执行步骤1，直到全部元素有序
     *
     *
     * 稳定算法，原地算法
     *
     * 时间复杂度
     * 最好：O(n)
     * 最坏、平均：O(n^2)
     *
     * 空间复杂度：O(1)
     */



}
